#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int	i;

	i = 0;
	c %= 256;
	while (s && s[i] && s[i] != c)
		i++;
	if (s && s[i] == c)
		return ((char *)&s[i]);
	return (0);
}
